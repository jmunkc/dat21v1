package variables_operators;

public class VariableExample {
    public static void main(String[] args) {

        int theLoneliestNumberSinceTheNumberOne = 2; //Storing the value 2
        double twoPointFive = 2.0; //Storing the value 2.5
        char theCharacterC = 'c'; //Storing the letter c

        System.out.println(theLoneliestNumberSinceTheNumberOne);
        System.out.println(twoPointFive);
        System.out.println(theCharacterC);
    }
}
